    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //Elimina la fila seleccionada.   
        DefaultTableModel model = (DefaultTableModel) this.TablaActividad.getModel();

        int fila = this.TablaActividad.getSelectedRow();

        if (fila < 0) 
        {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una fila de la tabla");
        } 
        else 
        {
            int confirmar = JOptionPane.showConfirmDialog(null, "Â¿Esta seguro que desea Eliminar el registro? ");

            if (JOptionPane.OK_OPTION == confirmar) 
            {

                String nombre=this.txtNombre.getText();
                String hora=this.txtHora.getText();
                String dia=this.txtDia.getText();
                String tipoActividad=this.cboTipoActividad.getSelectedItem().toString();
                String apenomProf=this.txtApellidoProfesor.getText();
                String apenomCliente=this.txtApellidoCliente.getText();
                
                ConexionJDBC.getInstance().EliminarActividad(nombre, hora, dia, tipoActividad, apenomProf, apenomCliente);
                model.removeRow(fila);
                JOptionPane.showMessageDialog(null, "Â¡Registro Eliminado!");
            }
        }
        this.CargarTabla();
        this.LimpiarCampos();
        
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void TablaActividadMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaActividadMouseClicked
        
        for (int i = 0; i < this.TablaActividad.getRowCount(); i++) 
            {
                String nomAct="",hora="",dia="",tipoAct="",apenomP = "",apenomC="";
               
                nomAct = (String) this.TablaActividad.getValueAt(this.TablaActividad.getSelectedRow(), 0);
                hora = (String) this.TablaActividad.getValueAt(this.TablaActividad.getSelectedRow(), 1);
                dia = (String) this.TablaActividad.getValueAt(this.TablaActividad.getSelectedRow(), 2);
                tipoAct = (String) this.TablaActividad.getValueAt(this.TablaActividad.getSelectedRow(), 3);
                apenomP = (String) this.TablaActividad.getValueAt(this.TablaActividad.getSelectedRow(), 4);
                apenomC = (String) this.TablaActividad.getValueAt(this.TablaActividad.getSelectedRow(), 5);

                this.txtNombre.setText(nomAct);
                this.txtHora.setText(hora);
                this.txtDia.setText(dia);
                
                this.cboTipoActividad.addItem(tipoAct);
                this.cboTipoActividad.setSelectedItem(tipoAct);
                
                this.txtApellidoProfesor.setText(apenomP);
                this.txtApellidoCliente.setText(apenomC);
                
            }
        
    }//GEN-LAST:event_TablaActividadMouseClicked

