        public void cargarComboTipoActividad() {
        ArrayList r = ConexionJDBC.getInstance().getTipoActividad();

        if (r != null) {
            try {
                DefaultComboBoxModel modelo = new DefaultComboBoxModel();
                Iterator iter = r.iterator();

                while (iter.hasNext()) {
                    modelo.addElement(iter.next());
                }
                this.cboTipoActividad.setModel(modelo);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "No se pudo cargar el combo Tipo Actividad");
            }
        } else {
            JOptionPane.showMessageDialog(this, "No tiene registrado Tipo Actividad");
        }
    }
    
    
