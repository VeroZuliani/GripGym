     private void CargarTabla() {

        DefaultTableModel tabla = new DefaultTableModel();
        try {
            tabla.addColumn("Nombre");
            tabla.addColumn("Hora");
            tabla.addColumn("DÃ­a");
            tabla.addColumn("Tipo Actividad");
            tabla.addColumn("Profesor");
            tabla.addColumn("Cliente");
           
            ResultSet rs = ConexionJDBC.getInstance().getCargarActividad();

            while (rs.next()) {
                Object dato[] = new Object[6];
                for (int i = 0; i < 6; i++) 
                {
                    dato[0]=rs.getString("Nombre");
                    dato[1]=rs.getString("Hora");
                    dato[2]=rs.getString("Dia");
                    dato[3]=rs.getString("TipoActividad");
                    dato[4]=rs.getString("Profesor");
                    dato[5]=rs.getString("Cliente");
                }
                tabla.addRow(dato);
            }
            this.TablaActividad.setModel(tabla);

        } catch (Exception e) {
        }
    }
    

