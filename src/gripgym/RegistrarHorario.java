        private void CargarTabla() {

        DefaultTableModel tabla = new DefaultTableModel();
        try {
            tabla.addColumn("Hora");
            tabla.addColumn("Dia");
           
            ResultSet rs = ConexionJDBC.getInstance().getCargarHoraDia();
         

            while (rs.next()) {
                Object dato[] = new Object[2];
                for (int i = 0; i < 2; i++) {
                    dato[i] = rs.getString(i + 2);

                }
                tabla.addRow(dato);
            }
            this.TablaHorarios.setModel(tabla);

        } catch (Exception e) {
        }
    }
    
        
        
          public void CargarTablaPorHora() 
     { //Paso el modelo vacio para q se resetee
        DefaultTableModel dtm=new DefaultTableModel();
        this.TablaHorarios.setModel(dtm);

        //Manejo de la tabla.
        String titColumna[];
        titColumna=new String[2];
//        titColumna[0]="Id Horario";
	titColumna[0]="Hora";
	titColumna[1]="Día";
       

        String hora=this.txtHora.getText();
        ArrayList lista =ConexionJDBC.getInstance().HorarioPorHora(hora);
        
        Object[][] data = new String[lista.size()][2];
        try
        {
        Iterator iter=lista.iterator();
        Horario H=null;
        int indice=0;

        while(iter.hasNext())
        {    H=(Horario)iter.next();
//             data[indice][0]= String.valueOf(H.getIdHorario());
             data[indice][0]= H.getHora();
             data[indice][1] = H.getDia();
            
             indice++;
        }//fin del ciclo..
        dtm=new DefaultTableModel(data,titColumna);
        this.TablaHorarios.setModel(dtm);
        } catch(Exception e)
          {System.out.println("Error al cargar la tabla!!!" + e.getMessage());}

    }
     
     
     
     
     
