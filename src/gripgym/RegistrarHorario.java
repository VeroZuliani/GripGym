    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

   //Elimina la fila seleccionada.   
        DefaultTableModel model = (DefaultTableModel) this.TablaHorarios.getModel();

        int fila = this.TablaHorarios.getSelectedRow();

        if (fila < 0) 
        {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una fila de la tabla");
        } 
        else 
        {
            int confirmar = JOptionPane.showConfirmDialog(null, "¿Esta seguro que desea Eliminar el registro? ");

            if (JOptionPane.OK_OPTION == confirmar) 
            {
                String hora=this.txtHora.getText();
                String dia=this.txtDia.getText();
                ConexionJDBC.getInstance().EliminarHorario(hora, dia);
                model.removeRow(fila);
                JOptionPane.showMessageDialog(null, "¡Registro Eliminado!");
            }
        }
        this.CargarTabla();
        this.LimpiarCampos();
        
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void TablaHorariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaHorariosMouseClicked
            
        for (int i = 0; i < this.TablaHorarios.getRowCount(); i++) 
            {
                String hora = "", dia = "";
               
               
                hora = (String) this.TablaHorarios.getValueAt(this.TablaHorarios.getSelectedRow(), 0);
                dia = (String) this.TablaHorarios.getValueAt(this.TablaHorarios.getSelectedRow(), 1);

                
                this.txtHora.setText(hora);
                this.txtDia.setText(dia);
            }
        
    }//GEN-LAST:event_TablaHorariosMouseClicked

