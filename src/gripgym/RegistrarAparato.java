        private void CargarTabla() {

        DefaultTableModel tabla = new DefaultTableModel();
        try {
            tabla.addColumn("Nombre Aparato");
            tabla.addColumn("Tipo");
            tabla.addColumn("Descripción");
//            tabla.addColumn("Nombre Rutina");
           
            ResultSet rs = ConexionJDBC.getInstance().getCargarAparato();

            while (rs.next()) {
                Object dato[] = new Object[3];
                for (int i = 0; i < 3; i++) {
                    dato[i] = rs.getString(i + 1);

                }
                tabla.addRow(dato);
            }
            this.TablaAparato.setModel(tabla);

        } catch (Exception e) {
        }
    }
     
     
