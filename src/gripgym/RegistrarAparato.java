    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        //Elimina la fila seleccionada.   
        DefaultTableModel model = (DefaultTableModel) this.TablaAparato.getModel();

        int fila = this.TablaAparato.getSelectedRow();

        if (fila < 0) 
        {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una fila de la tabla");
        } 
        else 
        {
            int confirmar = JOptionPane.showConfirmDialog(null, "¿Esta seguro que desea Eliminar el registro? ");

            if (JOptionPane.OK_OPTION == confirmar) 
            {
//                int dni=Integer.parseInt(this.txtDni.getText());
//                ConexionJDBC.getInstance().EliminarClienteC(dni);
//                ConexionJDBC.getInstance().EliminarClienteP(dni);
                model.removeRow(fila);
                JOptionPane.showMessageDialog(null, "¡Registro Eliminado!");
            }
        }
        this.CargarTabla();
        
    }//GEN-LAST:event_btnEliminarActionPerformed

