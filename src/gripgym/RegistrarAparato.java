    private void TablaAparatoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaAparatoMouseClicked
         for (int i = 0; i < this.TablaAparato.getRowCount(); i++) 
            {
                String nomA = "", tipo = "", desc="", nomR="";
               
               
                nomA= (String) this.TablaAparato.getValueAt(this.TablaAparato.getSelectedRow(), 0);
                tipo = (String) this.TablaAparato.getValueAt(this.TablaAparato.getSelectedRow(), 1);
                desc = (String) this.TablaAparato.getValueAt(this.TablaAparato.getSelectedRow(), 2);
//                nomR = (String) this.TablaAparato.getValueAt(this.TablaAparato.getSelectedRow(), 3);

                
                this.txtNom.setText(nomA);
                this.txtTipo.setText(tipo);
                this.txtDesc.setText(desc);
//                this.txtNombreRutina.setText(nomR);
            }
    }//GEN-LAST:event_TablaAparatoMouseClicked

