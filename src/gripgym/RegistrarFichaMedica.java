         private void CargarTabla() {

        DefaultTableModel tabla = new DefaultTableModel();
        try {
            tabla.addColumn("Nro Ficha");
            tabla.addColumn("D.N.I");
            tabla.addColumn("Cliente");
            tabla.addColumn("Edad");
            tabla.addColumn("Sexo");
            tabla.addColumn("Telefono");
            tabla.addColumn("Celular");
            tabla.addColumn("Tel Emergencia");
            tabla.addColumn("Grupo Sanguíneo");
            tabla.addColumn("Mutual");
            tabla.addColumn("Enfermedad");
            tabla.addColumn("Observación");

            ResultSet rs = ConexionJDBC.getInstance().getCargarTablaFichaMedica();

            while (rs.next()) {
                Object dato[] = new Object[12];
                for (int i = 0; i < 12; i++) {
                    dato[i] = rs.getString(i + 1);

                }
                tabla.addRow(dato);
            }
            this.TablaFichaMedica.setModel(tabla);

        } catch (Exception e) {
        }
    }
      
      

